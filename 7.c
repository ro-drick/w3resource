#include <stdio.h>

int main(){
    int a = 10;
    long ax = 1234567890;
    short s = 4043;
    float x = 4.132;
    double dx = 4.8989898;
    char c = 'W';
    unsigned long ux = 5654357567;

    printf("int: %d\n", a);
    printf("long: %ld\n", ax);
    printf("short: %d\n", s);
    printf("float: %f\n", x);
    printf("double: %lf\n", dx);
    printf("char: %c\n", c);
    printf("unsigned long: %lu\n", ux);

    return 0;
}
